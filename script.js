const archiveGrid = document.querySelector("#archiveGrid");
const lightbox = document.querySelector("#lightbox");
const lightboxImage = document.querySelector("#lightboxImage");
const closeLightbox = document.querySelector(".lightbox__close");
const languageButtons = document.querySelectorAll("[data-lang]");

let currentLang = localStorage.getItem("ayayLanguage") || "ru";
if (!translations[currentLang]) currentLang = "ru";

const imagePath = (key, index) =>
  `assets/posts/${key}/${String(index).padStart(2, "0")}.jpg`;

function t(path) {
  return path.split(".").reduce((value, key) => value?.[key], translations[currentLang]);
}

function openPreview(src, alt = "") {
  lightboxImage.src = src;
  lightboxImage.alt = alt;
  if (typeof lightbox.showModal === "function") {
    lightbox.showModal();
  } else {
    lightbox.setAttribute("open", "");
  }
}

function setFeatured(card, key, index) {
  const featuredButton = card.querySelector(".collection-card__image");
  const featuredImage = featuredButton.querySelector("img");
  const buttons = card.querySelectorAll(".thumb-row button");
  const nextSrc = imagePath(key, index);
  const title = t(`cards.${key}.title`);

  featuredImage.src = nextSrc;
  featuredImage.alt = `${title}, ${t("ui.slide")} ${index}`;
  featuredButton.dataset.lightbox = nextSrc;

  buttons.forEach((button) => {
    button.setAttribute("aria-current", button.dataset.index === String(index));
  });
}

function renderCollection(collection) {
  const title = t(`cards.${collection.key}.title`);
  const text = t(`cards.${collection.key}.text`);
  const card = document.createElement("article");
  card.className = "collection-card";
  card.dataset.key = collection.key;

  const firstImage = imagePath(collection.key, 1);
  card.innerHTML = `
    <button class="collection-card__image" type="button" data-lightbox="${firstImage}">
      <img src="${firstImage}" alt="${title}, ${t("ui.slide")} 1" loading="lazy">
    </button>
    <div class="collection-card__body">
      <h3>${title}</h3>
      <p>${text}</p>
      <div class="thumb-row" aria-label="${title}"></div>
    </div>
  `;

  const thumbRow = card.querySelector(".thumb-row");
  for (let index = 1; index <= collection.count; index += 1) {
    const button = document.createElement("button");
    const src = imagePath(collection.key, index);
    button.type = "button";
    button.dataset.index = String(index);
    button.setAttribute("aria-label", `${title}, ${t("ui.slide")} ${index}`);
    button.setAttribute("aria-current", index === 1 ? "true" : "false");
    button.innerHTML = `<img src="${src}" alt="" loading="lazy">`;
    button.addEventListener("click", () => setFeatured(card, collection.key, index));
    thumbRow.append(button);
  }

  return card;
}

function renderCollections() {
  archiveGrid.replaceChildren(...collections.map(renderCollection));
}

function applyLanguage(lang) {
  currentLang = translations[lang] ? lang : "ru";
  localStorage.setItem("ayayLanguage", currentLang);
  document.documentElement.lang = currentLang;
  document.title = t("meta.title");

  const description = document.querySelector('meta[name="description"]');
  if (description) description.content = t("meta.description");

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = t(element.dataset.i18n);
    if (value) element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const value = t(element.dataset.i18nAlt);
    if (value) element.alt = value;
  });

  lightbox.setAttribute("aria-label", t("meta.lightbox"));
  closeLightbox.setAttribute("aria-label", t("meta.close"));
  closeLightbox.title = t("meta.close");

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === currentLang;
    button.setAttribute("aria-pressed", String(isActive));
  });

  renderCollections();
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

document.addEventListener("click", (event) => {
  const trigger = event.target.closest("[data-lightbox]");
  if (!trigger) return;
  const image = trigger.querySelector("img");
  openPreview(trigger.dataset.lightbox, image?.alt || t("ui.archiveImage"));
});

closeLightbox.addEventListener("click", () => {
  lightbox.close();
});

lightbox.addEventListener("click", (event) => {
  if (event.target === lightbox) {
    lightbox.close();
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && lightbox.open) {
    lightbox.close();
  }
});

applyLanguage(currentLang);
